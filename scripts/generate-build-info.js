/**
 * Generates angular/src/app/shared/build-info.ts with version, build number,
 * and build date. Run before any Angular build (dev or production).
 */
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const pkg = require('../package.json');

let buildNumber = 'dev';
try {
  buildNumber = execSync('git rev-parse --short HEAD', { stdio: ['ignore', 'pipe', 'ignore'] })
    .toString()
    .trim();
} catch {
  buildNumber = `local-${Date.now().toString(36)}`;
}

const buildDate = new Date().toISOString();

const out = `// AUTO-GENERATED by scripts/generate-build-info.js — do not edit.
export const BuildInfo = {
  version: '${pkg.version}',
  buildNumber: '${buildNumber}',
  buildDate: '${buildDate}',
  author: '${pkg.author}',
} as const;
`;

const target = path.join(__dirname, '..', 'angular', 'src', 'app', 'shared', 'build-info.ts');
fs.writeFileSync(target, out);
console.log(`[build-info] ${pkg.version} · ${buildNumber} · ${buildDate}`);
